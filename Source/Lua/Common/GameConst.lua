local Register = function(name, lines, col_row, types)
	_G[name .. "FormationArray"] = {}
	_G[name .. "LineArray"] = {}
	_G[name .. "TypeArray"] = {}

	if (lines ~= nil) then
		for i = 1, #lines do
			_G[name .. "LineArray"]["Lines" .. i] = line_types[lines[i]]
		end
	end

	for i = 1, #col_row do
		_G[name .. "FormationArray"]["Formation" .. i] = {}
		for j = 1, col_row[i][1] do
			table.insert(_G[name .. "FormationArray"]["Formation" .. i], col_row[i][2])
		end
	end

	_G[name .. "TypeArray"] = types
end

