--简化手动输入连线的流程..不用打逗号了
local EasyLine = function(tab)
	local result = {}
	for i = 1, #tab do
		table.insert(result, {})
		local str = tostring(tab[i])
		for j = 1, #str do
			table.insert(result[i], tonumber(tostring(string.sub(str, j, j))))
		end
	end
	return result
end

